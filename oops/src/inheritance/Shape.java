package inheritance;

public class Shape {
	
	private String color;
	private int borderWidth;
	
	public void setColor(String color) {
		
		this.color=color;
		
		
		
	}
	
	
	public String getColor() {
		return color;
		
	}
	
	
	public void setBorder(int borderWidth) {
		
		this.borderWidth=borderWidth;
	}

	
	public int getBorder() {
		return borderWidth;
	}
	
	
	
}
